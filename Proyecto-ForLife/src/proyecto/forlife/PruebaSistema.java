package proyecto.forlife;

import javax.swing.JOptionPane;

/**
 *
 * @author lcast
 * Esta clase solo era para poder probar si la clase de sistema donde tenemos cargado a los empleados funcionaba.
 */
public class PruebaSistema {
    public static void main(String[] args) {
        Sistema sistema = new Sistema();

        String textoId = JOptionPane.showInputDialog("Ingrese el ID del empleado:");
        int id = Integer.parseInt(textoId);  // convierte el texto a n√∫mero

        Empleado e = sistema.buscarEmpleadoPorId(id);

        if (e != null) {
            JOptionPane.showMessageDialog(null,
                """
                Empleado encontrado:
                
                ID: """ + e.getId() + "\n" +
                "Nombre: " + e.getNombre() + "\n" +
                "Departamento: " + e.getDepartamento());
        } else {
            JOptionPane.showMessageDialog(null,
                "Empleado no encontrado con el ID: " + id);
        }
    }
    
}
